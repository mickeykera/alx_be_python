# Define the principal amount (initial investment)
principal = 1000

# Define the annual interest rate (as a decimal)
rate = 0.05

# Define the time the money is invested for in years
time = 3

# Calculate the simple interest using the formula: I = P * R * T
# I is the interest earned
# P is the principal amount
# R is the annual interest rate (as a decimal)
# T is the time the money is invested for in years
interest = principal * rate * time

# Print the calculated interest in the specified format
print(f"The simple interest is: {interest}")
